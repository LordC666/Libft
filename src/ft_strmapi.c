#include "../libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	size_t			j;
	char			*result;

	if (!s || !f)
		return ('\0');
	i = 0;
	len = ft_strlen(s);
	result = (char)malloc(len + 1);
	if (!result)
		return ('\0');
	while (i < len)
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[len] = '\0';
	return (result);
}
